    private void btn_TambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TambahActionPerformed
        String nopol = txt_nopol.getText();
        String unit = txt_unit.getText();
        String harga = txt_harga.getText();

        if (nopol.isEmpty() || unit.isEmpty() || harga.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mohon lengkapi semua data", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        con = Basisdata.getKoneksi();
        String sql = "INSERT INTO Kendaraan (No_Polisi, Unit, Harga_Sewa_Per_Hari) VALUES (?, ?, ?)";
        try{
            pst = con.prepareStatement(sql);
            pst.setString(1, nopol);
            pst.setString(2, unit);
            pst.setString(3, harga);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil ditambah", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        clear();
        update_table();
    }//GEN-LAST:event_btn_TambahActionPerformed

