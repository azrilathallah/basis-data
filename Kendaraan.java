    private void clear() {
        txt_nopol.setText("");
        txt_unit.setText("");
        txt_harga.setText("");
    }
    
    private void cari(){
        try{
            st = Basisdata.getKoneksi().createStatement();
            rs = st.executeQuery("SELECT * FROM Kendaraan WHERE No_Polisi LIKE '%"+txt_cari.getText()+"%'");
            dtm.getDataVector().removeAllElements();
            while(rs.next()){
                Object[] x = new Object[3];
                    x[0] = rs.getString("No_Polisi");
                    x[1] = rs.getString("Unit");
                    x[2] = rs.getString("Harga_Sewa_Per_Hari");
                    dtm.addRow(x);
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi error", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    private void update_table(){
        dtm.getDataVector().removeAllElements();
        dtm.fireTableDataChanged();
        try{
            con = Basisdata.getKoneksi();
            try(Statement s = con.createStatement()){
                String sql = "SELECT * FROM Kendaraan";
                rs = s.executeQuery(sql);
                while(rs.next()){
                    Object[] x = new Object[3];
                    x[0] = rs.getString("No_Polisi");
                    x[1] = rs.getString("Unit");
                    x[2] = rs.getString("Harga_Sewa_Per_Hari");
                    dtm.addRow(x);
                }
                rs.close();
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error");
        }
    }
    
