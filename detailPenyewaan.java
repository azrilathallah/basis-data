    private void cari(){
        try{
            st = Basisdata.getKoneksi().createStatement();
            rs = st.executeQuery("SELECT * FROM DetailPenyewaan WHERE No_Transaksi LIKE '%"+txt_cari.getText()+"%'");
            dtm.getDataVector().removeAllElements();
            while(rs.next()){
                Object[] x = new Object[9];
                    x[0] = rs.getString("No_Transaksi");
                    x[1] = rs.getString("No_Polisi");
                    x[2] = rs.getString("Tanggal_Sewa");
                    x[3] = rs.getString("Tanggal_Berakhir");
                    x[4] = rs.getString("Batas_Waktu");
                    x[5] = rs.getString("Total");
                    x[6] = rs.getString("Jam_Ambil");
                    x[7] = rs.getString("Jam_Kembali");
                    x[8] = rs.getString("Tujuan");
                    dtm.addRow(x);
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi error", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    private void update_table(){
        dtm.getDataVector().removeAllElements();
        dtm.fireTableDataChanged();
        try{
            con = Basisdata.getKoneksi();
            try(Statement s = con.createStatement()){
                String sql = "SELECT * FROM DetailPenyewaan";
                rs = s.executeQuery(sql);
                while(rs.next()){
                    Object[] x = new Object[9];
                    x[0] = rs.getString("No_Transaksi");
                    x[1] = rs.getString("No_Polisi");
                    x[2] = rs.getString("Tanggal_Sewa");
                    x[3] = rs.getString("Tanggal_Berakhir");
                    x[4] = rs.getString("Batas_Waktu");
                    x[5] = rs.getString("Total");
                    x[6] = rs.getString("Jam_Ambil");
                    x[7] = rs.getString("Jam_Kembali");
                    x[8] = rs.getString("Tujuan");
                    dtm.addRow(x);
                }
                rs.close();
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error");
        }
    }
    
    private void selisih(){
        try {
            SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            String tgl1 = date.format(tglSewa.getDate());
            String tgl2 = date.format(tglBerakhir.getDate());
            Date tglAwal = date.parse(tgl1);
            Date tglBerakhir = date.parse(tgl2);
            long hari1 = tglAwal.getTime();
            long hari2 = tglBerakhir.getTime();
            long bedaHari = hari2 - hari1;
            long btsWaktu = bedaHari / (24 * 60 * 60 * 1000);
            String hasil = (Long.toString(btsWaktu));
            txt_btsWaktu.setText(hasil);

            int harga_sewa = Integer.parseInt(lbl_harga1.getText()); 
            int btsWaktu_sewa = Integer.parseInt(txt_btsWaktu.getText());   
            int Total = harga_sewa * btsWaktu_sewa;                     
            String a = Integer.toString(Total);
            lbl_total.setText(a);
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Masukan Tanggal Sewa dan Tanggal Berakhir");
        }
    }
