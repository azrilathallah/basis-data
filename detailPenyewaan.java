    private void btn_TambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TambahActionPerformed
        SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
        String nomTrans = lbl_noTrans1.getText();
        String nopol = box_nopol.getSelectedItem().toString();
        String tgl1 = date.format(tglSewa.getDate());
        String tgl2 = date.format(tglBerakhir.getDate());
        String btsWaktu = txt_btsWaktu.getText();
        String total = lbl_total.getText();
        String jamAmbil = txt_jamAmbil.getText();
        String jamKembali = txt_jamKembali.getText();
        String tujuan = txt_tujuan.getText();

        if (tgl1==null || tgl2==null || jamAmbil.isEmpty() || jamKembali.isEmpty() || tujuan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mohon lengkapi semua data", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        con = Basisdata.getKoneksi();
        String sql = "INSERT INTO DetailPenyewaan (No_Transaksi, No_Polisi, Tanggal_Sewa, Tanggal_Berakhir, Batas_Waktu, Total, Jam_Ambil, Jam_Kembali, Tujuan) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
        try{
            pst = con.prepareStatement(sql);
            pst.setString(1, nomTrans);
            pst.setString(2, nopol);
            pst.setString(3, tgl1);
            pst.setString(4, tgl2);
            pst.setString(5, btsWaktu);
            pst.setString(6, total);
            pst.setString(7, jamAmbil);
            pst.setString(8, jamKembali);
            pst.setString(9, tujuan);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil ditambah", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        clear();
        update_table();
    }//GEN-LAST:event_btn_TambahActionPerformed

