    private void btn_TambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TambahActionPerformed
        String nama = txt_nama.getText();
        String telp = txt_telp.getText();
        String alamat = txt_alamat.getText();

        if (nama.isEmpty() || telp.isEmpty() || alamat.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mohon lengkapi semua data", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        con = Basisdata.getKoneksi();
        String sql = "INSERT INTO Penyewa (Nama_Penyewa, Nomor_Telepon, Alamat) VALUES (?, ?, ?)";
        try{
            pst = con.prepareStatement(sql);
            pst.setString(1, nama);
            pst.setString(2, telp);
            pst.setString(3, alamat);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil ditambah", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
        clear();
        update_table();
    }//GEN-LAST:event_btn_TambahActionPerformed

