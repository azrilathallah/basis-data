    private void update_table(){
        dtm.getDataVector().removeAllElements();
        dtm.fireTableDataChanged();
        try{
            con = Basisdata.getKoneksi();
            try(Statement s = con.createStatement()){
                String sql = "SELECT * FROM Penyewa";
                rs = s.executeQuery(sql);
                while(rs.next()){
                    Object[] x = new Object[3];
                    x[0] = rs.getString("Nama_Penyewa");
                    x[1] = rs.getString("Nomor_Telepon");
                    x[2] = rs.getString("Alamat");
                    dtm.addRow(x);
                }
                rs.close();
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error");
        }
    }
    
    private void cari(){
        try{
            st = Basisdata.getKoneksi().createStatement();
            rs = st.executeQuery("SELECT * FROM Penyewa WHERE Nama_Penyewa LIKE '%"+txt_cari.getText()+"%'");
            dtm.getDataVector().removeAllElements();
            while(rs.next()){
                Object[] x = new Object[3];
                    x[0] = rs.getString("Nama_Penyewa");
                    x[1] = rs.getString("Nomor_Telepon");
                    x[2] = rs.getString("Alamat");
                    dtm.addRow(x);
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi error", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    
