    public void nama(){
        try{
            con = Basisdata.getKoneksi();
            st = con.createStatement();
            String sql = "SELECT Nama_Penyewa FROM Penyewa";
            rs = st.executeQuery(sql);
            
            while(rs.next()){
                String nama = rs.getString("Nama_Penyewa");
                box_nama.addItem(nama);
            }
        }
        catch(SQLException e){
            Logger.getLogger(Penyewaan.class.getName()).log(Level.SEVERE, null, e);
        }
    }

