    private void btn_HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_HapusActionPerformed
        String nomTrans = txt_noTrans.getText();
        
        if (nomTrans.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mohon isi nomor transaksi yang ingin dihapus", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String sqlCekNomTrans = "SELECT COUNT(*) FROM Penyewaan WHERE No_Transaksi=?";
        try {
            pst = con.prepareStatement(sqlCekNomTrans);
            pst.setString(1, nomTrans);
            rs = pst.executeQuery();
            rs.next();
            int count = rs.getInt(1);
            if (count == 0) {
                JOptionPane.showMessageDialog(this, "Nomor Transaksi tidak ditemukan", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        int selectedOption = JOptionPane.showConfirmDialog(null, "Hapus data dengan nomor Transaksi : "+nomTrans+"?","Konformasi", JOptionPane.INFORMATION_MESSAGE);
        if(selectedOption == JOptionPane.YES_OPTION){
            con = Basisdata.getKoneksi();
            try{
                String sql = "DELETE FROM Penyewaan WHERE No_Transaksi=?";
                pst = con.prepareStatement(sql);
                pst.setString(1, nomTrans);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
            } 
            catch(SQLException e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        clear();
        update_table();
    }//GEN-LAST:event_btn_HapusActionPerformed

