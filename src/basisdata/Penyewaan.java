    private void cari(){
        try{
            st = Basisdata.getKoneksi().createStatement();
            rs = st.executeQuery("SELECT * FROM Penyewaan WHERE No_Transaksi LIKE '%"+txt_cari.getText()+"%'");
            dtm.getDataVector().removeAllElements();
            while(rs.next()){
                Object[] x = new Object[7];
                    x[0] = rs.getString("No_Transaksi");
                    x[1] = rs.getString("Nama_Penyewa");
                    x[2] = rs.getString("Pemilik_Unit");
                    x[3] = rs.getString("Keterangan");
                    x[4] = rs.getString("Alamat_Pengambilan");
                    x[5] = rs.getString("Alamat_Pengembalian");
                    x[6] = rs.getString("Jaminan");
                    dtm.addRow(x);
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi error", "Pemberitahuan", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    private void update_table(){
        dtm.getDataVector().removeAllElements();
        dtm.fireTableDataChanged();
        try{
            con = Basisdata.getKoneksi();
            try(Statement s = con.createStatement()){
                String sql = "SELECT * FROM Penyewaan";
                rs = s.executeQuery(sql);
                while(rs.next()){
                    Object[] x = new Object[7];
                    x[0] = rs.getString("No_Transaksi");
                    x[1] = rs.getString("Nama_Penyewa");
                    x[2] = rs.getString("Pemilik_Unit");
                    x[3] = rs.getString("Keterangan");
                    x[4] = rs.getString("Alamat_Pengambilan");
                    x[5] = rs.getString("Alamat_Pengembalian");
                    x[6] = rs.getString("Jaminan");
                    dtm.addRow(x);
                }
                rs.close();
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error");
        }
    }

